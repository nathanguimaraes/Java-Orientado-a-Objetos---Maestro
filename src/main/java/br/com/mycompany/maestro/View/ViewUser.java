    private void btn_attActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_attActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) tb_user.getModel();
            model.setNumRows(0);
            this.users = userRN.listaTodos();
            for (User user : users) {
                String[] linha = {String.valueOf(user.getCodigo() ), String.valueOf(user.getId()), user.getSenha()};
                model.addRow(linha);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_attActionPerformed

    private void btn_altActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_altActionPerformed
        int linha = this.tb_user.getSelectedRow();
        Long id = Long.valueOf(tb_user.getValueAt(linha, 0).toString());
        new CadUser(id).setVisible(true);
    }//GEN-LAST:event_btn_altActionPerformed

