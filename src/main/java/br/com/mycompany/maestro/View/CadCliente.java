    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed

        try{
            if(this.idAlteracao == 0){
                cli.setNome(txt_nome.getText());
                cli.setStatus(checkAtivo.isSelected());
                cli.setCpf(txt_cpf.getText());
                cli.setRg(txt_rg.getText());

                Calendar datanasCalendar = jd_dtnas.getCalendar();
                LocalDate datanasLocalDate = LocalDateTime.ofInstant(datanasCalendar.toInstant(), datanasCalendar.getTimeZone().toZoneId()).toLocalDate();
                cli.setDtnas(datanasLocalDate);

                if(rb_fem.isSelected()){
                    cli.setSexo(Sexo.FEMININO);
                }
                else
                {
                    cli.setSexo(Sexo.MASCULINO);
                }

                cli.setEmail(txt_email.getText());
                cli.setTelefone(txt_telefone.getText());
                end.setBairro(txt_bairro.getText());
                end.setCep(txt_cep.getText());
                end.setCidade(txt_cidade.getText());
                end.setComplemento(txt_complemento.getText());
                end.setEstado(txt_estado.getText());
                end.setLogradouro(txt_logradouro.getText());
                end.setNumero(txt_numero.getText());
                cli.setEndereco(end);

                this.clienteRN.salvar(cli);
            }else{
                Cliente cliRecuperado = clienteRN.listarUm(idAlteracao);
                cliRecuperado.setNome(this.txt_nome.getText());
                cliRecuperado.setStatus(checkAtivo.isSelected());
                cliRecuperado.setCpf(txt_cpf.getText());
                cliRecuperado.setRg(txt_rg.getText());

                Calendar datanasCalendar = jd_dtnas.getCalendar();
                LocalDate datanasLocalDate = LocalDateTime.ofInstant(datanasCalendar.toInstant(), datanasCalendar.getTimeZone().toZoneId()).toLocalDate();
                cliRecuperado.setDtnas(datanasLocalDate);

                if(rb_fem.isSelected()){
                    cliRecuperado.setSexo(Sexo.FEMININO);
                }
                else
                {
                    cliRecuperado.setSexo(Sexo.MASCULINO);
                }

                cliRecuperado.setEmail(txt_email.getText());
                cliRecuperado.setTelefone(txt_telefone.getText());
                end.setBairro(txt_bairro.getText());
                end.setCep(txt_cep.getText());
                end.setCidade(txt_cidade.getText());
                end.setComplemento(txt_complemento.getText());
                end.setEstado(txt_estado.getText());
                end.setLogradouro(txt_logradouro.getText());
                end.setNumero(txt_numero.getText());
                cliRecuperado.setEndereco(end);

                this.clienteRN.atualizar(cliRecuperado);
            }
            JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

