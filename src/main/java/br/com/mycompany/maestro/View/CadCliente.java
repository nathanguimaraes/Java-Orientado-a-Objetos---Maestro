    public CadCliente(Long idAlteracao) {
        this.clienteRN = new ClienteRN();
        this.cli = new Cliente();
        this.end = new Endereco();
        initComponents();
        setLocationRelativeTo(null);
        this.idAlteracao = idAlteracao;
        try{
            Cliente cliRecuperado = clienteRN.listarUm(idAlteracao);
            txt_id.setText(String.valueOf(cliRecuperado.getCodigo()));
            txt_nome.setText(cliRecuperado.getNome());
            txt_bairro.setText(cliRecuperado.getEndereco().getBairro());
            txt_cep.setText(cliRecuperado.getEndereco().getCep());
            txt_cidade.setText(cliRecuperado.getEndereco().getCidade());
            txt_complemento.setText(cliRecuperado.getEndereco().getComplemento());
            txt_estado.setText(cliRecuperado.getEndereco().getEstado());
            txt_cpf.setText(cliRecuperado.getCpf());
            txt_rg.setText(cliRecuperado.getRg());
            txt_email.setText(cliRecuperado.getEmail());
            txt_telefone.setText(cliRecuperado.getTelefone());
            jd_dtnas.setDate(DataUtil.converteLocalDateToDate(cliRecuperado.getDtnas()));
            if(cliRecuperado.getSexo() == sex.FEMININO){
                rb_fem.setSelected(true);
            }
            else
            {
                rb_masc.setSelected(true);
            }
            txt_logradouro.setText(cliRecuperado.getEndereco().getLogradouro());
            txt_numero.setText(cliRecuperado.getEndereco().getNumero());
            checkAtivo.setSelected(cliRecuperado.getStatus());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    


