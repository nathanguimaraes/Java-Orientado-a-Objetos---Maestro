      private void popularComboEstadoProcessoEdicao(Long id) {
        ComboBoxModel modelosts;
            try {
                    Vector linhas = new Vector();

                    Vector concluido = new Vector();
                    concluido.add(EstadoProcesso.CONCLUIDO);
                    linhas.add(concluido);

                    Vector analise = new Vector();
                    analise.add(EstadoProcesso.ANALISE);
                    linhas.add(analise);

                    Vector andamento = new Vector();
                    andamento.add(EstadoProcesso.ANDAMENTO);
                    linhas.add(andamento);

                    Vector cancelado = new Vector();
                    cancelado.add(EstadoProcesso.CANCELADO);
                    linhas.add(cancelado);

                    modelosts = new DefaultComboBoxModel(linhas);
                    cb_estadoProcesso.setModel(modelosts);
                    
                if (ps.getEstadoProcesso().equals(EstadoProcesso.CONCLUIDO)) {
                    cb_estadoProcesso.setSelectedIndex(0);
                } 
                else if (ps.getEstadoProcesso().equals(EstadoProcesso.ANALISE))
                {
                    cb_estadoProcesso.setSelectedIndex(1);
                }
                else if (ps.getEstadoProcesso().equals(EstadoProcesso.ANDAMENTO))
                {
                    cb_estadoProcesso.setSelectedIndex(2);
                }
                else
                {
                    cb_estadoProcesso.setSelectedIndex(3);
                }
            } catch (Exception e) {

                    e.printStackTrace();
            }
    }
      
          private Object getEstadoProcessoSelecionado(int posicao) 
    {
        EstadoProcesso ep;
        if (cb_estadoProcesso.getSelectedIndex() == 0) 
        {
            ep = EstadoProcesso.CONCLUIDO;
        } 
        else if (cb_estadoProcesso.getSelectedIndex() == 1)  
        {
            ep = EstadoProcesso.ANALISE;
        }
        else if (cb_estadoProcesso.getSelectedIndex() == 2)  
        {
            ep = EstadoProcesso.ANDAMENTO;
        }
        else
        {
            ep = EstadoProcesso.CANCELADO;
        }
        
        return ep;
    }
