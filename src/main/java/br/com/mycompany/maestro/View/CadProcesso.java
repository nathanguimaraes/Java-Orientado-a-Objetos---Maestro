    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
              try{
            if(this.idAlteracao == 0){
                ps = new Processo();
                ps.setCausa(ta_pro.getText());
                ps.setIndiciado(ta_p.getText());
                ps.setServicosExtras(ta_s.getText());
                ps.setValort(new Double(txt_valort.getText()));

                Calendar dataentCalendar = jd_ent.getCalendar();
                LocalDate dataentLocalDate = LocalDateTime.ofInstant(dataentCalendar.toInstant(), dataentCalendar.getTimeZone().toZoneId()).toLocalDate();
                ps.setDtent(dataentLocalDate);

                Calendar datasaiCalendar = jd_sai.getCalendar();

                LocalDate datasaiLocalDate = LocalDateTime.ofInstant(datasaiCalendar.toInstant(), datasaiCalendar.getTimeZone().toZoneId()).toLocalDate();
                ps.setDtsaida(datasaiLocalDate);

                ps.setCliente((Cliente)this.getClienteSelecionado(cb_cliente.getSelectedIndex()));
                ps.setEstagiario((Estagiario)this.getEstagiarioSelecionado(cb_estager.getSelectedIndex()));
                ps.setAcao((Acao)this.getAcaoSelecionado(cb_acao.getSelectedIndex()));
                ps.setEstadoProcesso((EstadoProcesso)this.getEstadoProcessoSelecionado(cb_estadoProcesso.getSelectedIndex()));

                processoRN.salvar(ps);

            }else{
                ps.setCausa(ta_pro.getText());
                ps.setIndiciado(ta_p.getText());
                ps.setServicosExtras(ta_s.getText());
                ps.setValort(new Double(txt_valort.getText()));

                Calendar dataentCalendar = jd_ent.getCalendar();
                LocalDate dataentLocalDate = LocalDateTime.ofInstant(dataentCalendar.toInstant(), dataentCalendar.getTimeZone().toZoneId()).toLocalDate();
                ps.setDtent(dataentLocalDate);

                Calendar datasaiCalendar = jd_sai.getCalendar();
                LocalDate datasaiLocalDate = LocalDateTime.ofInstant(datasaiCalendar.toInstant(), datasaiCalendar.getTimeZone().toZoneId()).toLocalDate();
                ps.setDtsaida(datasaiLocalDate);

                ps.setCliente((Cliente)this.getClienteSelecionado(cb_cliente.getSelectedIndex()));
                ps.setEstagiario((Estagiario)this.getEstagiarioSelecionado(cb_estager.getSelectedIndex()));
                ps.setAcao((Acao)this.getAcaoSelecionado(cb_acao.getSelectedIndex()));
                ps.setEstadoProcesso((EstadoProcesso)this.getEstadoProcessoSelecionado(cb_estadoProcesso.getSelectedIndex()));

                processoRN.atualizar(ps);
            }
            
            Correio correio = new Correio();
            correio.NotificarPorEmail(ps);
            JOptionPane.showMessageDialog(this, "Processo cadastrado com sucesso, Verifique sua caixa de email!");
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

