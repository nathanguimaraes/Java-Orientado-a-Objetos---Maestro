    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        try{
            int linha = this.tb_acao.getSelectedRow();
            Long id = Long.valueOf(tb_acao.getValueAt(linha, 0).toString());
            Acao lanFind = acaoRN.listarUm(id);
            acaoRN.excluir(lanFind);

            DefaultTableModel model = (DefaultTableModel) tb_acao.getModel();
            model.removeRow(linha);
            JOptionPane.showMessageDialog(this, "Ação Excluída com Sucesso!");

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

