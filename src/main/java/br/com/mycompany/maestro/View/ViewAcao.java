    private void btn_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarActionPerformed
        String filtro = txt_desc.getText();

        try {
            DefaultTableModel model = (DefaultTableModel) tb_acao.getModel();
            model.setNumRows(0);
            this.acoes = acaoRN.listarFitroLike(filtro);

            for (Acao ac : acoes) {
                String[] linha = {String.valueOf(ac.getCodigo()), ac.getDescricao(), ac.getOrdem().getDescricao(), ac.getServicoPrestado().getDescricao()};
                model.addRow(linha);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_pesquisarActionPerformed

