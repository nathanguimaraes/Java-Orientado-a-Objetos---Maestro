    private void btn_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarActionPerformed
        String filtro = txt_nome.getText();

        try {
            DefaultTableModel model = (DefaultTableModel) tb_consulE.getModel();
            model.setNumRows(0);
            this.estagiario = estagiarioRN.listarFitroLike(filtro);

            for (Estagiario estager : estagiario) {
                String[] linha = {estager.getNome(), estager.getCpf(), estager.getRg(), String.valueOf(estager.getDtnas()), String.valueOf(estager.getSexo()), estager.getEndereco().getLogradouro(), estager.getEndereco().getNumero(),
                    estager.getEndereco().getComplemento(), estager.getEndereco().getCep(), estager.getEndereco().getCidade(), estager.getEndereco().getBairro(), estager.getEndereco().getEstado(),estager.getEmail(), estager.getTelefone(), String.valueOf(estager.getSalario())};
                model.addRow(linha);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_pesquisarActionPerformed

