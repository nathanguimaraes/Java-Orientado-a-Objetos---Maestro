    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        int linha = this.tb_consulE.getSelectedRow();
        Long id = Long.valueOf(tb_consulE.getValueAt(linha, 0).toString());
        new CadEstagiario(id).setVisible(true);
    }//GEN-LAST:event_btn_alterarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        try{
            int linha = this.tb_consulE.getSelectedRow();
            Long id = Long.valueOf(tb_consulE.getValueAt(linha, 0).toString());
            Estagiario estagerFind = estagiarioRN.listarUm(id);
            estagiarioRN.excluir(estagerFind);

            DefaultTableModel model = (DefaultTableModel) tb_consulE.getModel();
            model.removeRow(linha);
            JOptionPane.showMessageDialog(this, "Estagiario Excluído com Sucesso!");
            this.setClosable(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

